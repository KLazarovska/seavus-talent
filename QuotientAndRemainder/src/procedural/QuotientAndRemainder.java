package procedural;

public class QuotientAndRemainder {

	/**
	 * @param args
	 */

	public static void main(String[] args) {
		int number1 = 10;
		int number2 = 5;
		
		System.out.println("Number 1 = " + number1 + "\n" + "Number 2 = " + number2);
		System.out.println("Quotient = " + number1 / number2 + "\t" + "Remainder = " + number1 % number2);

	}

}
